package com.jgoodie;

public class Main {

    public static void main(String[] args) {
        // Keywords and expressions
        // 1 mile is = to 1.609344 kilometers
        double miles = 130.0;
        double kilometers = (miles * 1.609344);
        if (kilometers >= 200 && kilometers < 210) {
            System.out.println("Congratulations on riding a double century!");
        }


    }
}
